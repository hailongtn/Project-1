                            <p class="lead">
                                The Golden Gate Bridge is a suspension bridge spanning the Golden Gate, the one-mile-wide (1.6 km) strait connecting San Francisco Bay and the Pacific Ocean. The structure links the U.S. city of San Francisco, California—the northern tip of the San Francisco Peninsula—to Marin County, carrying both U.S. Route 101 and California State Route 1 across the strait. It also carries pedestrian and bicycle traffic, and is designated as part of U.S. Bicycle Route 95. Recognized by the American Society of Civil Engineers as one of the Wonders of the Modern World,[7] the bridge is one of the most internationally recognized symbols of San Francisco and California.. </p> 
    
                            <p>
                                The idea of a fixed link between San Francisco and Marin had gained increasing popularity during the late 19th century, but it was not until the early 20th century that such a link became feasible. Joseph Strauss served as chief engineer for the project, with Leon Moisseiff, Irving Morrow and Charles Ellis making significant contributions to its design. The bridge opened to the public in 1937 and has undergone various retrofits and other improvement projects in the decades since.

                                The Golden Gate Bridge is described in Frommer's travel guide as "possibly the most beautiful, certainly the most photographed, bridge in the world."[8][9] At the time of its opening in 1937, it was both the longest and the tallest suspension bridge in the world, titles it held until 1964 and 1998 respectively. Its main span is 4,200 feet (1,280 m) and its total height is 746 feet (227 m).[10]
                                
                                
                            </p>
    
    
    
                          >
    
                            <p>
                                Before the bridge was built, the only practical short route between San Francisco and what is now Marin County was by boat across a section of San Francisco Bay. A ferry service began as early as 1820, with a regularly scheduled service beginning in the 1840s for the purpose of transporting water to San Francisco.[11]

                                In 1867, the Sausalito Land and Ferry Company opened. In 1920, the service was taken over by the Golden Gate Ferry Company, which merged in 1929 with the ferry system of the Southern Pacific Railroad, becoming the Southern Pacific-Golden Gate Ferries, Ltd., the largest ferry operation in the world.[11][12] Once for railroad passengers and customers only, Southern Pacific's automobile ferries became very profitable and important to the regional economy.[13] The ferry crossing between the Hyde Street Pier in San Francisco and Sausalito Ferry Terminal in Marin County took approximately 20 minutes and cost $1.00 per vehicle prior to 1937, when the price was reduced to compete with the new bridge.[14][15] The trip from the San Francisco Ferry Building took 27 minutes.</p>
    
                          
                            

                                
                         
